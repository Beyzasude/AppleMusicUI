import Foundation

class FirstMusic {
    var musicDetail:String?
    var category:String?
    var appleMusic:String?
    var image:String?
    
    init(musicDetail: String, category: String, appleMusic: String, image: String) {
        self.musicDetail = musicDetail
        self.category = category
        self.appleMusic = appleMusic
        self.image = image
    }
}
